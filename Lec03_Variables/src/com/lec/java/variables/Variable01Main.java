package com.lec.java.variables;

public class Variable01Main {

	public static void main(String[] args) {
		System.out.println("변수(Variables) 01");
		
		// 변수 선언
		// 변수타입 변수이름;
		// int - 변수타입(정수형, integer)
		// num1, num2 - 변수이름
		int num1;
		int num2;
		
		// 변수의 초기화, 변수에 값을 저장
		num1 = 10;
		num2 = 20;
		
		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);
		
		// num1에 123을 저장
		num1 = 123;
		System.out.println("num1 = " + num1);
		
		// num2에 100을 저장
		num2 = 100;
		System.out.println("num2 = " + num2);
		
		// num1 + num2 결과를 화면에 출력
		System.out.println("num1 + num2 = " + (num1 + num2));
		
		// num1 - num2 결과를 화면에 출력
		System.out.println("num1 - num2 = " + (num1 - num2));
		
		// num1 * num2 결과를 화면에 출력
		System.out.println("num1 * num2 = " + (num1 * num2));
		
		// num1 / num2 결과를 화면에 출력
		System.out.println("num1 / num2 = " + (num1 / num2));

	} // end main()

} // end class Variable01Main





