package com.lec.java.inherit07;

public class Person {
	// 멤버 변수
	private String name;
	
	
	// getter & setter
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	// 메소드
	public void showInfo() {
		System.out.println("이름: " + name);
	}
	
} // end class Person









