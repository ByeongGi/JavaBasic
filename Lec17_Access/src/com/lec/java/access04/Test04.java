package com.lec.java.access04;

public class Test04 {

	private void privateMethod() {
		System.out.println("Test04 - private method");
	}
	
	void defaultMethod() {
		System.out.println("Test04 - default method");
	}

	protected void protectedMethod() {
		System.out.println("Test04 - protected method");
	}
	
	public void publicMethod() {
		System.out.println("Test04 - public method");
	}
	
} // end class Test04









