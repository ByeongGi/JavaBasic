package com.lec.java.inheritance;

public class SuperClass {
	// 멤버 변수 생성자 
	
	protected int  sup1=1;
	protected int  sup2=2;
	
	// 생성자 
	protected SuperClass(){
		System.out.println(" 수퍼 클래스의 상속자 ");
	}
	
		
		
	
	
	// 메소드 
	
	protected void SupMethod() {
		System.out.println("수퍼클래스 메소드다 !");
	}
	
	
	
	
}
